package EvenOdd;

public class EvenOdd {
  public static void main(String[] args) {
    int no = 12;
    {
      /*
       * % means no is divided by 2 and remainder should be 0
       */
    }
    if (no % 2 == 0) {
      System.out.println("No is Even");
    } else {
      System.out.println("No is Odd");
    }
  }
}
